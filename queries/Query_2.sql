SELECT REGION_NAME
FROM REGIONS REGIONES
WHERE NOT EXISTS(SELECT *
                 FROM COUNTRIES
                          NATURAL JOIN LOCATIONS
                          NATURAL JOIN DEPARTMENTS
                 WHERE REGION_ID = REGIONES.REGION_ID);



